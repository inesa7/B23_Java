package day21_Loops;

public class WhileLoopPractice2 {
    public static void main(String[] args) {
        int i = 0;
        while(i<=100){
            if(i%2==0) {
                System.out.println(i + " ");
            }

            i++;
        }

        System.out.println("-----------------------");

        for (int j = 1; j <= 10; j++) {
            if(j%2==0) {
                System.out.println(j + " ");
            }

        }




    }
}
