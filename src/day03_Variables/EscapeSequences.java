package day03_Variables;
/*
\n: new line (Enter on keyboard)
\t: paragraph space (tab)
\": double quote
\\: backslash
 */

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello everyone. \nHow are you all today?");

        System.out.println("----------------------------------------");

        System.out.println("My name is Inesa\nI am from Kazakhstan\nI live in Maryland");

        System.out.println("------------------------------");
        System.out.println("\tToday is a great day to study Java");

        System.out.println("------------------------------");
        System.out.println("My favorite TV series is \"Game of Throne\"");
    }

}
