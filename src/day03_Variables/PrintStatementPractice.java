package day03_Variables;

public class PrintStatementPractice {

    public static void main(String[] args) {

        System.out.print("Hello"); //will not be printed on a new line
        System.out.println("Hola"); //will be printed on a new line
        System.out.println("How are you?");
        System.out.print("Today is Friday"); //no new line, will be printed next to the last word
        System.out.println("Tomorrow is Saturday");

    }
}
